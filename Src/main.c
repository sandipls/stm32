/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
	uint8_t my_dhcp_retry = 0;
	uint8_t dispip = 0;
	DHCP_init(SOCK_DHCP, gDATABUF, HOSTNAME);
	LCD_Display_DHCPR();
  /* Infinite loop */
  for(;;)
  {
	 switch(DHCP_run())
		{
			case DHCP_IP_ASSIGN:
			case DHCP_IP_CHANGED:
				break;
			case DHCP_IP_LEASED:
				if(!dispip) {
					LCD_Display_IP();
					dispip = 1;
				}
				break;
			case DHCP_FAILED:
				my_dhcp_retry++;
				if(my_dhcp_retry > MY_MAX_DHCP_RETRY)
				{
					my_dhcp_retry = 0;
					DHCP_stop();      // if restart, recall DHCP_init()
				}
				break;
			default:
				break;
		}
	 DHCP_time_handler();
	 osDelay(1000);
  }
  /* USER CODE END 5 */
}

